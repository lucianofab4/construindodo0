/* Render simple markdown subset */
function renderContent(text) {
  return text.trim().split('\n').map((line, i) => {
    if (line.startsWith('## ')) return <h2 key={i} className="text-2xl font-bold text-zinc-100 mt-10 mb-4">{line.slice(3)}</h2>;
    if (line.startsWith('**') && line.endsWith('**')) return <p key={i} className="font-semibold text-zinc-200 leading-relaxed mb-4">{line.slice(2, -2)}</p>;
    if (line.startsWith('---')) return <hr key={i} className="border-zinc-800 my-8" />;
    if (line === '') return <br key={i} />;
    // Bold inline
    const parts = line.split(/(\*\*[^*]+\*\*)/g).map((p, j) =>
      p.startsWith('**') && p.endsWith('**')
        ? <strong key={j} className="text-zinc-200">{p.slice(2, -2)}</strong>
        : p
    );
    return <p key={i} className="text-zinc-400 leading-relaxed mb-4">{parts}</p>;
  });
}

